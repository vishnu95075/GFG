#include <iostream>
#include <string>

int countSchrodingerSmileys(const std::string& str) {
    int count = 0;
    int n = str.length();

    for (int i = 0; i < n - 1; i++) {
        if (str[i] == ':' && str[i + 1] == ')') {
            int j = i + 2;
            while (j < n && str[j] == ')') {
                j++;
            }
            if ( str[j] == ':') {
                count++;
            }
        }
    }

    return count;
}

int main() {
    int T;
    std::cin >> T; 

    while (T--) {
        int N;
        std::cin >> N; 

        std::string S;
        std::cin >> S; 

        int result = countSchrodingerSmileys(S); 

        std::cout << result << std::endl; 
    }

    return 0;
}